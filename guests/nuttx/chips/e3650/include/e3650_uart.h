#ifndef __CHIPS_E3650_INCLUDE_E3650_UART_H
#define __CHIPS_E3650_INCLUDE_E3650_UART_H

#include <nuttx/config.h>

#define E3650_UART_BASE        0xF8970000UL
#define E3650_UART_INTR0_OFF   0x20U
#define E3650_UART_INTEN0_OFF  0x30U
#define E3650_UART_FSR0_OFF    0x60U
#define E3650_UART_FSR1_OFF    0x64U
#define E3650_UART_TXDR_OFF    0x200U
#define E3650_UART_RXDR_OFF    0x300U

#define E3650_UART_FSR0_TX_FULL   (1U << 25)
#define E3650_UART_FSR1_RX_EMPTY  (1U << 24)
#define E3650_UART_INTR0_RXFWF    (1U << 1)
#define E3650_UART_INTEN0_RXFWFE  (1U << 1)

#define E3650_UART_RX_INTMASK     E3650_UART_INTR0_RXFWF

#define E3650_UART_IRQ            117
#define E3650_UART_IRQ_PRIO       20 /* 20 << 3 == 0xA0 */

#endif /* __CHIPS_E3650_INCLUDE_E3650_UART_H */
